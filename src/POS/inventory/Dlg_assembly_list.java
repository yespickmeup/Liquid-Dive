    private void ok1() {
        int id = -1;
        int prod_num = -1;
        String description = tf_name.getText();
        double price = FitIn.toDouble(tf_price.getText());
        double product_qty = FitIn.toDouble(tf_qty.getText());
        String ref_num = "0";
        int status = 0;

        S2_assembly.to_assembly to = new to_assembly(id, prod_num, description, price, product_qty, ref_num, status);
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(to));
        }
    }
