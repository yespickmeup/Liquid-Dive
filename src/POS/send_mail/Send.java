    private void show_dialog() {
        FileDialog d = new FileDialog(this, "");
        d.show();
        String dir = d.getDirectory();
        String file = d.getFile();
        String path = dir + file;
        path = path.replace("\\", "\\\\");
//        System.out.println(dir);
        lbl_path.setText(path);
    }
    
    private void send_email() {
        String path = lbl_path.getText();
        if (path.isEmpty()) {
            return;
        }
        String senderEmail = "";
        String recipientEmail = System.getProperty("recipientEmail", "maytopacka@gmail.com");
        String subject = "Cashier_report : " + DateType.day_and_time.format(new Date());
        String message = "";
        try {
            setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            MailClient.sendMail(senderEmail, recipientEmail, subject, message, path);
            //        String path;
        } catch (MessagingException ex) {
            Prompt.call("Failed to Send");
//            Logger.getLogger(Send.class.getName()).
//                    log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(Send.class.getName()).
                    log(Level.SEVERE, null, ex);
        }
        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
    }
